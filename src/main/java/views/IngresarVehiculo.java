    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try{
        String patente = PatenteTextField.getText();
        String modelo = ModeloTextField.getText();
        int anio = Integer.parseInt(AñoTextField.getText());
        double capacidad = Double.parseDouble(CapacidadTextField.getText());
        Marca marca = (Marca) jComboBox3.getSelectedItem();
        Sucursal suc = (Sucursal) jComboBox2.getSelectedItem();
        String tipo = jComboBox1.getSelectedItem().toString();
        if (tipo.equals("Electrico")){
            double kwh = Double.parseDouble(jTextField7.getText());
            Controlador.ingresarVehiculo(patente, marca, modelo, anio, capacidad, suc, domain.VehiculoTipo.ELECTRICO, kwh, 0);   
        }
        else{
            double kmL = Double.parseDouble(jTextField5.getText());
            double extra = Double.parseDouble(jTextField6.getText());
            Controlador.ingresarVehiculo(patente, marca, modelo, anio, capacidad, suc, domain.VehiculoTipo.COMBUSTIBLE, kmL, extra);
        }
        Limpiar();
       }
       catch (Exception e){
            System.out.println(e); 
            }
    }//GEN-LAST:event_jButton1ActionPerformed

