    private void comboLlenar(){
        ArrayList<Marca> marcas = Persistencia.getMarcas();
        for (Marca m : marcas) {
            jComboBox3.addItem(m);
        }
        ArrayList<Sucursal> sucursales = Persistencia.getSucursales();
        for (Sucursal s : sucursales){
            jComboBox2.addItem(s);
        }
        jComboBox1.addItem("Electrico");
        jComboBox1.addItem("Combustible");
    }
    
